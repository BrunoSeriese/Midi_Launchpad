int16_t mapDtoInt16(double x, double in_min, double in_max, int16_t out_min, int16_t out_max) {
    double result;
    result = (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;
    return result;
}

int16_t mapDtoInt16(double x, double in_min, double in_max) {
    int16_t out_max = 32767;
    int16_t out_min = -32767;

    return mapDtoInt16(x, in_min, in_max, out_min, out_max);
}

